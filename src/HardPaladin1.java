    @Override
    public void playerAttack() {
    
            goblinDamage = random.nextInt(50) + playerLevel * 7;
            undeadDamage = random.nextInt(50) + playerLevel * 7;

            if (goblinHP > 0) {
                goblinHP = (goblinHP > goblinDamage) ? goblinHP - goblinDamage : 0;
                System.out.println("Mage dealt " + goblinDamage + " damage to Goblin.");
                if (goblinHP == 0) {
                    System.out.println("Goblin is defeated!");
                    experiencePoints += 25;
                    System.out.println("Gained 25 experience");
                }
            }

            if (undeadHP > 0) {
                undeadHP = (undeadHP > undeadDamage) ? undeadHP - undeadDamage : 0;
                System.out.println("Mage dealt " + undeadDamage + " damage to Undead.");
                if (undeadHP == 0) {
                    System.out.println("Undead is defeated!");
                    experiencePoints += 30;
                    System.out.println("Gained 30 experience!");
                }
            }

}
    

    @Override
    public void opponentsAttack() {
        StringBuilder attackMessages = new StringBuilder();
    if (goblinHP > 0 && undeadHP > 0) {
                System.out.println("\nGoblin and Undead attack Mage!");
            } else if (goblinHP > 0) {
                System.out.println("\nGoblin attacks Mage!");
            } else if (undeadHP > 0) {
                attackMessages.append("\nUndead attacks Mage!");
            }

            if (goblinHP > 0) {
                int goblinDamage = random.nextInt( 31) + floorDamage;
                playerHP -= goblinDamage;
                System.out.println("Goblin dealt " + goblinDamage + " damage to Mage.");
            }

            if (undeadHP > 0) {
                int undeadDamage = random.nextInt(  51) + floorDamage;
                playerHP -= undeadDamage;
                System.out.println("Undead dealt " + undeadDamage + " damage to Mage.");
            }

    txtDia.append(attackMessages.toString());  // Appends the complete set of attack details at once
    }

