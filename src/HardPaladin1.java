private void initPlayerAttack() {
    Timer attackTimer = new Timer(1000, new ActionListener() {
        private int count = 0;

        @Override
        public void actionPerformed(ActionEvent e) {
            switch (count) {
                case 0:
                    playerAttack();
                    if (areOpponentsAlive() == 0) {
                        ((Timer) e.getSource()).stop(); // Stop the timer if no opponents are alive
                    }
                    break;
                case 1:
                    opponentsAttack();
                    if (isPlayerAlive() == 0) {
                        System.out.println("\nGame Over!");
                        ((Timer) e.getSource()).stop(); // Stop the timer if player is dead
                    }
                    break;
                default:
                    ((Timer) e.getSource()).stop(); // Stop the timer after all actions are processed
                    break;
            }
            count++;
        }
    });
    attackTimer.setInitialDelay(0); // Start immediately
    attackTimer.start();
}
